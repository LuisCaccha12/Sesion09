    void actualizarEstudiante(Estudiante estudiante) {
        for(int i = 0; i < Semana09.estudiantesGlobal.size(); i++)
        {
            Estudiante es = Semana09.estudiantesGlobal.get(i);
            if(es.hashCode()==estudiante.hashCode())
            {
                Semana09.estudiantesGlobal.set(i, estudiante);
            }
        }
        actualizarLista();
    }

    void agregarEstudiante(Estudiante estudiante) {
        
        Semana09.estudiantesGlobal.add(estudiante);
        actualizarLista();
    }

    void actualizarLista()
    {
         modelo.clear();
         for(Estudiante es : Semana09.estudiantesGlobal)
         {
             modelo.addElement(es.getTexto());
         }
    }//GEN-LAST:event_btnAgregarActionPerformed

    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        
        int index = lstEstudiantes.getSelectedIndex();
        if(index > -1)
        {
        Estudiante es = Semana09.estudiantesGlobal.get(index);
        
        EditarEstudiantes pantalla = new EditarEstudiantes(this, es);
        pantalla.setVisible(true);
    
        this.setVisible(false);
        }
        
    }//GEN-LAST:event_btnEditarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        
        int index = lstEstudiantes.getSelectedIndex();
        if(index > -1)
        {
            Estudiante es = Semana09.estudiantesGlobal.get(index);
        
            int option = JOptionPane.showConfirmDialog(this, "Â¿Deseas eliminar a " +es.getNombre(),"?",JOptionPane.WARNING_MESSAGE);
            if(option == JOptionPane.OK_OPTION)
            {
                Semana09.estudiantesGlobal.remove(es);
            }
            actualizarLista();
        }
        
    }//GEN-LAST:event_btnEliminarActionPerformed

